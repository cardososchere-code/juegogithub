    <p>las estructuras repetitivas de java son : For , While, Do-While que son utilizadas para ejecutar un bloque de código varias veces según una condición</p>
    <h3>Atributos y Metodos de POO</h3>
    <p> los ATRIBUTOS son variables que pertenecen a una clase que definen las propiedades de un objeto.
